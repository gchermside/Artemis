# For Loop Syntax

for x in range (10):
    print("Hello")

#you get to control how many times you want something to be printed out

#WHILE Loops

age=0

while age<16:
    print (age)
    age=age+1

print("I can get my driver's license!")


#Infinite Loop Syntax

numCookies=0

while numCookies<10:
    print("I have" + str(numCookies) + "cookies")

#you are never changing the variable's so it goes forever. 0 is always less than 10
